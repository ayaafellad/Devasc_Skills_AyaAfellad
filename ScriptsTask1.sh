# Command for initializing a Git Repository:
git init

# Command for connecting Local Repository to Github Repository:
git remote add origin https://github.com/ayaafellad/Devasc_Skills_AyaAfellad.git

# Commands for adding and committing changes:
git add .
git commit -m "Initial commit"

# Command for pushing changes to GitHub:
git push -u origin master

# Commands to add, commit, and push "Task 1 -- Github Skills Test"
git add .
git commit -m "Task X: Task 1 -- Github Skills Test"
git push

# Command to add a tag to a specific commit:
git tag -a task1 -m "task1_Github_Skills_Test"

# Command to push the tag to GitHub:
git push origin task1
